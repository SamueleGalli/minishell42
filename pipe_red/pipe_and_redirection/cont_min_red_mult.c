#include "../../minishell.h"

int	min_com(t_env *e)
{
	int	i;

	i = e->i;
	e->i--;
	while (e->i > 0 && e->v[e->i][0] != '|' && e->v[e->i][0] != '<'
		&& e->v[e->i][0] != '>')
		e->i--;
	if (e->i == 0 && e->v[e->i][0] != '>' && e->v[e->i][0] != '<')
	{
		e->i = i;
		return (1);
	}
	else if (e->v[e->i][0] == '|' || e->v[e->i][0] == '<'
		|| e->v[e->i][0] == '>')
	{
		e->i++;
		e->i = i;
		return (1);
	}
	return (0);
}
