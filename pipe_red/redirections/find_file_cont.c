#include "../../minishell.h"

char	*find_mult_filepath(t_env *e)
{
	int		i;

	i = e->i;
	while (e->v[i] != NULL)
	{
		if (compare(e->v[i], ">") == 1)
		{
			i++;
			break ;
		}
		i++;
	}
	return (e->v[i]);
}
