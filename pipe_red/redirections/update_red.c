#include "../../minishell.h"

int	update_redir(t_env *e)
{
	if (e->v[e->i] == NULL)
		return (0);
	while (e->v[e->i] != NULL && \
	e->v[e->i][0] != '|')
		e->i++;
	return (1);
}
