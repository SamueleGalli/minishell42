#include "../../minishell.h"

void	after_file_exe(t_env *e)
{
	if ((compare(e->v[e->tmp_i], "echo") == 1 || \
	compare(e->v[e->tmp_i], "echo ") == 1) && e->v[e->i + 1] != NULL && \
	e->v[e->i][0] != '>' && e->v[e->i][0] != '<' && e->v[e->i][0] != '|')
	{
		while (e->v[e->i] != NULL && e->v[e->i][0] != '>')
			e->i++;
		e->i = e->i + 2;
		printf(" %s\n", e->v[e->i]);
	}
}
