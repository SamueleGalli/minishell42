#include "../../minishell.h"

void	update_pipe(t_env *e)
{
	if (e->v[e->i] != NULL)
	{
		while (e->v[e->i] != NULL && e->v[e->i][0] != '|' \
		&& e->v[e->i][0] != '<' && e->v[e->i][0] != '>')
			e->i++;
		if (e->v[e->i] == NULL)
			return ;
		if (e->v[e->i][0] == '<' || e->v[e->i][0] == '>')
			return ;
		if (e->v[e->i] != NULL)
			e->i++;
	}
}
