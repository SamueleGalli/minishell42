#include "../minishell.h"

void	print_no_quote(char *s, int exist)
{
	int	i;

	i = 0;
	if (s[i] == '\'' || s[i] == '\"')
		i++;
	while (s[i] != 0 && s[i] != '\'' && s[i] != '\"')
	{
		printf("%c", s[i++]);
		if (exist == 1 && s[i] == ' ')
			i++;
	}
}
